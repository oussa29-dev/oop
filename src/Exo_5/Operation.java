package src.Exo_5;

public class Operation {

    public static int somme(int i,int j){return i+j;}
    public static int difference(int i,int j){return i-j;}
    public static int produit(int i,int j){return i*j;}
    public static double moyenne(int i,int j){return (i+j)/2;}
    public static int plusGrand(int i,int j){if(i>j)return i; else return j;}
}
